#include "ft_printf_private.h"

ssize_t	ft_printf_strichr(const char *s, char c)
{
	ssize_t i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
